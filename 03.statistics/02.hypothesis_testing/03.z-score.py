"""
Z-score represents the standardized distance of the sample mean from the 
population mean.
It follows the normal distribution.

z = (x̄ - μ) / (σ / √n)
Where:
  x̄ : Sample mean
  μ : Population mean (under the null hypothesis)
  σ : Population standard deviation
  n : Sample size

if x̄=μ=0 then z=0 then we accept the null hypothesis.
if z falls into rejection region then we reject the null hypothesis.
"""

"""
What is z-standardization?
Z-standardization is a statistical procedure used to make data points from different 
datasets comparable. 
In this procedure, each data point is converted into a z-score. 
A z-score indicates how many standard deviations a data point is from the mean of the dataset.
"""